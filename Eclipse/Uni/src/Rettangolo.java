
public class Rettangolo {
	private int altezza;
	private int base;
	Punto vertice;
	
	public void setBase(int b) {
		base=b;
	}
	public void setAltezza(int a) {
		altezza=a;
	}
	public void setVertice(Punto v) {
		vertice=v;
	}
	
	public int getBase() {
		return base;
	}
	public int getAltezza() {
		return altezza;
	}
	
	public Punto getVertice() {
		return vertice;
	}
	
	
	
}
